void InicializarRelays(void){
	setDir(RELAY0_P, OUTPUT);
	setDir(RELAY1_P, OUTPUT);
	setDir(RELAY2_P, OUTPUT);
	setDir(RELAY3_P, OUTPUT);

	setPinmode_OP(RELAY0_P, MODE_OP_NHIGH);
	setPinmode_OP(RELAY1_P, MODE_OP_NHIGH);
	setPinmode_OP(RELAY2_P, MODE_OP_NHIGH);
	setPinmode_OP(RELAY3_P, MODE_OP_NHIGH);

	setPin(RELAY0_P, !OFF);
	setPin(RELAY1_P, !OFF);
	setPin(RELAY2_P, !OFF);
	setPin(RELAY3_P, !OFF);
}

void setRelay(uint8_t n, uint8_t state){
	if(n == RELAY0)			setPin(RELAY0_P, !state);
	else if(n == RELAY1)	setPin(RELAY1_P, !state);
	else if(n == RELAY2)	setPin(RELAY2_P, !state);
	else if(n == RELAY3)	setPin(RELAY3_P, !state);

}
